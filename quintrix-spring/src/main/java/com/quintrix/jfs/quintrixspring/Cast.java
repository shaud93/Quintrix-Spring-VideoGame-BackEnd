package com.quintrix.jfs.quintrixspring;

public class Cast {
	private final int id;
	private final String actor;
	
	public Cast(int id, String actor) {
		this.id = id;
		this.actor = actor;
	}
	
	public int getId() {
		return id;
	}


	public String getActor() {
		return actor;
	}

}
